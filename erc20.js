async function isERC20(name, address) {
    var code = web3.eth.getCode(address)
    if (code === '0x')
        console.log(name, ' is not an ERC20 token address')
    else
        console.log(name, ' ERC20 token detected')
}

function printWei(inp) {
    const format = web3.utils.fromWei(inp);
    return format;
}

async function printToken(address) {
    const contract = new common.web3.eth.Contract(erc20abi, address);
    const decimals = await contract.methods.decimals().call();
    const name = await contract.methods.name().call();
    let s = await contract.methods.symbol().call();
    let total = await contract.methods.totalSupply().call();

    //calculate actual tokens amounts based on decimals in token
    const amountToSendinDecimal = total / ( Math.pow(10, decimals) );

    console.log("Name: ", name)
    console.log("====================")
    console.log("Symbol: ", s);
    console.log("Address: ", address)
    console.log("Decimals: ", decimals);
    console.log("Raw Total: ", total);
    console.log("Total Supply: ", amountToSendinDecimal.toLocaleString());
    console.log("====================\n")
}

